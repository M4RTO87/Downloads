---INFORME 1---

SELECT 
      NOMBRE_EMP||' '||APPATERNO_EMP||' '||APMATERNO_EMP AS "NOMBRE EMPLEADO",
      SUELDO_EMP AS "SUELDO ACTUAL",
      SUELDO_EMP*0.085 AS "AUMENTO",
      SUELDO_EMP*1.085 AS"SUELDO REAJUSTADO"
FROM 
    EMPLEADO
ORDER BY 2 DESC

---INFORME 2---

SELECT 
      NOMBRE_EMP||' '||APPATERNO_EMP||' '||APMATERNO_EMP AS "NOMBRE EMPLEADO",
      SUELDO_EMP AS "SUELDO ACTUAL",
      SUELDO_EMP*0.20 AS "AUMENTO",
      SUELDO_EMP*1.20 AS "SUELDO REAJUSTADO"
FROM 
    EMPLEADO
WHERE SUELDO_EMP >= 200000 AND SUELDO_EMP<=800000
ORDER BY 2 DESC

