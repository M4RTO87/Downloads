---INFORME 1---
SELECT 
     FECHA_ENTREGA_PROPIEDAD AS "FECHA ENTREGA PROPIEDAD"
FROM PROPIEDAD
WHERE FECHA_ENTREGA_PROPIEDAD BETWEEN '01/01/25' AND '31/12/25'
ORDER BY FECHA_ENTREGA_PROPIEDAD ASC

---INFORME 2---

SELECT 
    FECINI_ARRIENDO AS "FECHA INICIO ARRIENDO"
FROM PROPIEDAD_ARRENDADA
WHERE FECINI_ARRIENDO BETWEEN '01/01/25' AND '31/12/25'
ORDER BY FECHA_ENTREGA_PROPIEDAD ASC

----- CONSULTA 2 -----

SELECT
    NUMRUT_CLI||'-'||DVRUT_CLI AS "RUT CLIENTE",
    NOMBRE_CLI||' '||APPATERNO_CLI||' '||APMATERNO_CLI AS "NOMBRE CLIENTE",
    RENTA_CLI AS "RENTA",
    FONOFIJO_CLI AS "FONO FIJO",
    CELULAR_CLI AS "CELULAR"
FROM
    CLIENTE
WHERE RENTA_CLI >= 800000 AND ID_ESTCIVIL IN(1,3,4)
ORDER BY APPATERNO_CLI,NOMBRE_CLI ASC

